<h1>Motivation</h1>

<p>
Die erste Frage die sicht stellt ist: "Warum noch ein neues GUI Toolkit? Gibt es nicht schon genug?"
Die klarer Anwtwort darauf ist: "Für unsere Bedüfnisse hat leider genau dieses noch gefehlt.".
</p>

<p>
In WebFrap herrscht eine strikte Trennung zwischen Layout und Code. Bei vielen JS Gui Toolkits ist es 
jedoch nur schwer unsere Definition von dieser Trennung mit angemessenem Aufwand zu implementieren.
Darüber hinaus hab es leider noch keine umfassendes Toolkit mit der gewünschten Architektur, der
passenden Lizenz und welches darüber hinaus auch noch auf jQuery basiert.
</p>

<p>
Es gibt jedoch neben den technischen Kriterien noch ein weiteres wichtiges
Kriterium. Die Kontrolle über die zukünftige Entwicklung des Toolkits.<br />
Wir haben ein hohes Entwicklungstempo, und sehr spezielle Anforderungen.
Auf der anderen sind wir im Moment noch nicht wichtig genug, dass "Fremdentwickler"
sich in irgendeiner Weise genötigt sehen würden darauf Rücksicht zu nehmen.
</p>

<p>
Kurz, das was wir brauchen gibt es nicht, und wenn es das zu einem bestimmten Zeitpunkt gäbe
ist fraglich, dass es sich in der Form weiterentwickelt welche für uns nötig wäre.
</p>
