<h1>Fokus</h1>

<p>
WGT ist auf die Erstellung von RIA (Richt Internet Applications) optimiert.
</p>

<p>
Der Fokus liegt dabei auf gut benutzbar Formular und Listenelemente, sowie soezialisierte
Eingabeelemente wie Gantt Charts für Planungsszenarien, Kallender elemente, WYSIWYG Editoren
für Inhalte, sowie dynamischen Graphen/Charts sowie viele andere Business relevante
Elemente.
</p>

<p>
Ziel ist es, einfach bedienbare, ästhetisch anmutende Business Applikationen zu erstellen, 
welche dem Anwender aktiv helfen sein in der Regel ständig komplexer werdente Aufgaben
flüssig und stressfrei zu erfüllen.
</p>

